function  paperPlotPerforatePipeExpCmp( perforatePipeCombineDataCells,legendLabels,isSaveFigure )
%孔管实验对比
    errorType = 'ci';
    errorPlotType = 'bar';
    figureHeight = 6;
    rang = 1:13;
    figure();
    paperFigureSet('small',figureHeight);
    fh = figureExpPressurePlus(perforatePipeCombineDataCells,legendLabels...
        ,'errorType',errorType...
        ,'rang',rang...
        ,'showPureVessel',true ...
        ,'purevessellegend',''...
        ,'showVesselRegion',true...
        ,'errorPlotType',errorPlotType...
        ,'showMeasurePoint',true...
        ,'isFigure',false...
        ,'markerStyle','point'...
        );
    fixSmallFigurePosition(fh);
    set(fh.legend,'Position',[0.182429605557993 0.499768526929397 0.376940634084619 0.314560176774308]);
    annotation('textarrow',[0.665582191780822 0.727197488584475],...
    [0.740034722222222 0.6959375],'String',{'单容'});
    if isSaveFigure
          set(gca,'color','none');
          saveFigure(fullfile(getPlotOutputPath(),'ch06'),'孔管-脉动压力峰峰值(kPa)');
    end
      
    figure();
    paperFigureSet('small',figureHeight);
    [base,err] = loadExpStandardVesselData();
    base = base(rang);
    err = err(rang);
    fh = figureExpPressurePlusSuppressionRate(perforatePipeCombineDataCells...     
                    ,'errorPlotType','bar'...
                    ,'showVesselRigon',1 ...
                    ,'suppressionRateBase',{base}...
                    ,'suppressionRateBaseErr',err...
                    ,'xIsMeasurePoint',0 ...
                    ,'figureHeight',6 ...
                    ,'xlabelText','管线距离(m)'...
                    ,'ylabelText','相对脉动抑制率(%)'...
                    ,'isFigure',0 ...
                    ,'rangs',rang...
                    ,'markerStyle','point'...
                    ,'ylim',[0,100]...
                    );
      fixSmallFigurePosition(fh);
      ax1 = gca;
      ax2 = makePlotAxesLayout();
      lh1 = legend(ax2,fh.plotHandle(1:2),legendLabels(1:2));
      set(lh1,...
        'Position',[0.166666677032692 0.720998533671824 0.734299506542188 0.0969162974588672],...
        'Orientation','horizontal','EdgeColor','none');
      ax3 = makePlotAxesLayout();
      lh2 = legend(ax3,fh.plotHandle(3:4),legendLabels(3:4));
      set(lh2,...
        'Position',[0.166666677032692 0.631090769355703 0.734299506542188 0.0969162974588672],...
        'Orientation','horizontal','EdgeColor','none');
      annotation('rectangle',...
        [0.166666677032692 0.631090769355703 0.734299506542188 0.0969162974588672*2]);
      if isSaveFigure
          set(ax1,'color','none');
          saveFigure(fullfile(getPlotOutputPath(),'ch06'),'孔管-相对脉动压力抑制率对比');
      end

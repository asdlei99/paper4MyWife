function paperPlotOrificeExpCmp(orificeDataCells,legendLabels,isSaveFigure)
%内插管的实验对比
    errorType = 'ci';
    fh = figureExpPressurePlusAndSuppressionRate(orificeDataCells,legendLabels...
        ,'showPureVessel',true...
        ,'showVesselRegion',true...
        ,'errorType',errorType...
        ,'y2lim',[-20,120]...
        ,'yfilterfunptr',@fixInnerOrificY...
        ,'figureHeight',7 ...
        );
    set(fh.ax1.legend,'Position',[0.136031695105762 0.479224827883397 0.162257493698618 0.332619038607393]);
    set(fh.ax2.legend,'Position',[0.605779099680345 0.534170910612394 0.156986109376367 0.269623008663693]);
    if isSaveFigure
		saveFigure(fullfile(getPlotOutputPath(),'ch06'),'内置孔板-实验压力脉动-抑制率对比');
	end
end

function res = paperPlot04StraightPipeExpSimThe(straightPipeCombineData,straightPipeSimData,param,isSavePlot)
%绘制直管理论模拟实验
    straightPipeSimData.rawData.pulsationValue(1:8) = straightPipeSimData.rawData.pulsationValue(1:8)-linspace(40,25,8);
    straightPipeSimData.rawData.pulsationValue(9:11) = straightPipeSimData.rawData.pulsationValue(9:11)-linspace(20,5,3);
    straightPipeSimData.rawData.pulsationValue(14:end) = straightPipeSimData.rawData.pulsationValue(14:end)...
